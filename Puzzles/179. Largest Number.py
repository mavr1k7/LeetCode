class Solution:
    def largestNumber(self, nums):
        m = {}
        for n in nums:
            x = str(n)[0]
            if x not in m:
                m[x] = []
            m[x].append(n)
        long_num = ""
        while m:
            k = max(m)
            ints = self.weighted_sort(m.pop(k))
            for i in ints:
                long_num += str(i)
        return str(int(long_num))    

    def weighted_sort(self, lst):
        s = []
        m = len(str(max(lst)))
        for n in lst:
            n = str(n)
            rep = n[0]
            mod = False
            while len(n) < m:
                n += rep
                mod = True
            n = int(n)
            if mod:
                n += 0.5
            s.append(n)
        z = [x for _,x in sorted(zip(s,lst), reverse=True)]
        for i in range(len(lst) - 1):
            o = str(z[i])
            p = str(z[i+1])
            a = int(o + p)
            b = int(p + o)
            if b > a:
                z[i] = int(p)
                z[i+1] = int(o)
        return z
